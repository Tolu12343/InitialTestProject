
public class Calculator {
	
   public static  double addition (double firstNum, double secondNum) {
	   double result = firstNum + secondNum;
	   return result;
   }
   public static  double substraction(double firstNum, double secondNum) {
	   double result = firstNum - secondNum;
	   return result;
   }
   public static  double mult(double firstNum, double secondNum) {
	   double result = firstNum * secondNum;
	   return result;
   }
   public static double division(double firstNum, double secondNum) {
	   double result = firstNum / secondNum;
	   return result;
   }
}
